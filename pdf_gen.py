    story.append(dt)
    story.append(Spacer(1, 4*mm))

    # Footer note
    story.append(HRFlowable(width="100%", thickness=1, color=GOLD))
    story.append(Paragraph(
        "This Kundli is generated using Lahiri Ayanamsha and Whole-Sign House system. "
        "Planetary positions are approximate. Please consult a qualified Jyotishi for detailed analysis.",
        styles["Small"]))

    doc.build(story, onFirstPage=header_footer, onLaterPages=header_footer)
