def make_styles():
    styles = getSampleStyleSheet()
    styles.add(ParagraphStyle("Title1", fontName="Helvetica-Bold", fontSize=22,
        textColor=DARK_RED, alignment=TA_CENTER, spaceAfter=6))
    styles.add(ParagraphStyle("Title2", fontName="Helvetica-Bold", fontSize=16,
        textColor=MAROON, alignment=TA_CENTER, spaceAfter=4))
    styles.add(ParagraphStyle("SectionHead", fontName="Helvetica-Bold", fontSize=13,
        textColor=DARK_RED, spaceBefore=10, spaceAfter=4,
        borderPad=4, backColor=LIGHT_GOLD))
    styles.add(ParagraphStyle("Normal1", fontName="Helvetica", fontSize=10,
        textColor=colors.black, spaceAfter=3))
    styles.add(ParagraphStyle("Small", fontName="Helvetica", fontSize=8,
        textColor=colors.grey))
    styles.add(ParagraphStyle("Center", fontName="Helvetica", fontSize=10,
        alignment=TA_CENTER))
